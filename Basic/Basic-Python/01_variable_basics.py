# مشروع: أساسيات المتغيرات (Variable Basics)
# ---------------------------------------------------------

# تعريف متغيرات بأنواع مختلفة
name = "Mohamed"      # نص (String)
age = 25              # رقم صحيح (Integer)
height = 1.75         # رقم عشري (Float)
is_active = True      # قيمة منطقية (Boolean)

# طباعة المتغيرات مع معرفة نوع كل واحد
print("Name:", name, "Type:", type(name))
print("Age:", age, "Type:", type(age))
print("Height:", height, "Type:", type(height))
print("Active status:", is_active, "Type:", type(is_active))

